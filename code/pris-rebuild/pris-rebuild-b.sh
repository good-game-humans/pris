    --enable-shared --enable-threads=posix \\
    --enable-__cxa_atexit --enable-languages=c,c++ \\
    --disable-libstdcxx-pch --disable-bootstrap"
    cmd "make"
    cmd "make install"
    cmd "echo 'main(){}' > dummy.c"
    cmd "cc dummy.c"
    cmd "readelf -l a.out | grep ': /tools'"
    cmd "rm -v dummy.c a.out"
