    cmd "mkdir -v ../gcc-build"
    cmd "cd ../gcc-build"
    cmd "CC=\"gcc -B/usr/bin/\" ../gcc-4.3.2/configure --prefix=/tools \\
    --with-local-prefix=/tools --disable-nls --enable-shared --disable-libssp \\
    --enable-languages=c"
    cmd "make"
    cmd "make install"
    cmd "ln -vs libgcc.a \`gcc -print-libgcc-file-name | \\
    sed 's/libgcc/&_eh/'\`"
