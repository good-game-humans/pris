set +h
umask 022
LFS=/mnt/lfs
LC_ALL=POSIX
PATH=/tools/bin:/bin:/usr/bin
export LFS LC_ALL PATH
EOF"

echo_cmd "source ~/.bash_profile"

# Instead of sourcing the newly-created file, which would put us in a new 
# shell, set env manually.
