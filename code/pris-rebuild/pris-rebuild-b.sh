    cmd "find \${GCC_FIXED}/* -maxdepth 0 -xtype d -exec rm -rvf '{}' \;"
    cmd "rm -vf \`grep -l \"DO NOT EDIT THIS FILE\" \${GCC_FIXED}/*\`"
    cmd "unset GCC_FIXED"
    cmd "echo 'main(){}' > dummy.c"
    cmd "cc dummy.c"
    cmd "readelf -l a.out | grep ': /tools'"
    cmd "rm -v dummy.c a.out"
