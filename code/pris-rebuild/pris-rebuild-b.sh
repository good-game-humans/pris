    cmd "sed 's/^XCFLAGS =$/& -fomit-frame-pointer/' gcc/Makefile.in.tmp \\
    > gcc/Makefile.in"
    cmd "for file in \$(find gcc/config -name linux64.h -o -name linux.h)
do
  cp -uv \$file{,.orig}
  sed -e 's@/lib\(64\)\?\(32\)\?/ld@/tools&@g' \\
  -e 's@/usr@/tools@g' \$file.orig > \$file
  echo \"
#undef STANDARD_INCLUDE_DIR
#define STANDARD_INCLUDE_DIR 0\" >> \$file
  touch \$file.orig
done"
    get_pkg_file ${MPFR_PKG}
    cmd "tar xvf ../${PKG_FILE}"
    cmd "mv mpfr-2.3.2 mpfr"
    get_pkg_file ${GMP_PKG}
    cmd "tar xvf ../${PKG_FILE}"
    cmd "mv gmp-4.2.4 gmp"
    cmd "mkdir -v ../gcc-build"
