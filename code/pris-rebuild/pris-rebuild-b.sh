    download_pkg ${BINUTILS_PKG}
    unpack_pkg ${BINUTILS_PKG}
    download_patches ${BINUTILS_PATCH0}
    cmd "cd ${PKG_WORKDIR}"
    cmd "patch -Np1 -i ../${PATCH_FILES[0]}"
    [[ -e ../binutils-build ]] && rm -rf ../binutils-build
    cmd "mkdir -v ../binutils-build"
    cmd "cd ../binutils-build"
    cmd "CC=\"gcc -B/usr/bin/\" ../binutils-2.18/configure \\
    --prefix=/tools --disable-nls --disable-werror"
    cmd "make"
    cmd "make install"
    cmd "make -C ld clean"
    cmd "make -C ld LIB_PATH=/tools/lib"
    cmd "cp -v ld/ld-new /tools/bin"
    cmd "cd .."
    cmd "rm -rf ${PKG_WORKDIR} binutils-build"
    place_marker "tools-binutils-pass1"
